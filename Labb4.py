# --- –§—É–Ω–∫—Ü—ñ—è –¥–ª—è —Ñ–æ—Ä–º–∞—Ç—É–≤–∞–Ω–Ω—è —Ü—ñ–Ω–∏ ---
def format_price(price):
    """
    –ü—Ä–∏–π–º–∞—î —á–∏—Å–ª–æ —Ç–∞ –ø–æ–≤–µ—Ä—Ç–∞—î —Ä—è–¥–æ–∫ —É —Ñ–æ—Ä–º–∞—Ç—ñ '—Ü—ñ–Ω–∞: xxx.xx –≥—Ä–Ω'.
    """
    return f"—Ü—ñ–Ω–∞: {price:.2f} –≥—Ä–Ω"


# --- –§—É–Ω–∫—Ü—ñ—è –¥–ª—è –ø–µ—Ä–µ–≤—ñ—Ä–∫–∏ –Ω–∞—è–≤–Ω–æ—Å—Ç—ñ —Ç–æ–≤–∞—Ä—ñ–≤ ---
def check_availability(*products):
    """
    –ü—Ä–∏–π–º–∞—î –¥–æ–≤—ñ–ª—å–Ω—É –∫—ñ–ª—å–∫—ñ—Å—Ç—å —Ç–æ–≤–∞—Ä—ñ–≤.
    –ü–æ–≤–µ—Ä—Ç–∞—î —Å–ª–æ–≤–Ω–∏–∫ –ª–∏—à–µ –¥–ª—è –ø–µ—Ä–µ–¥–∞–Ω–∏—Ö —Ç–æ–≤–∞—Ä—ñ–≤:
    –∫–ª—é—á ‚Äî –Ω–∞–∑–≤–∞ —Ç–æ–≤–∞—Ä—É, –∑–Ω–∞—á–µ–Ω–Ω—è ‚Äî True –∞–±–æ False (—î / –Ω–µ–º–∞—î).
    """
    available = {
        "—Ö–ª—ñ–±": True,
        "–º–æ–ª–æ–∫–æ": True,
        "—è–±–ª—É–∫–∞": False,
        "—Ü—É–∫–æ—Ä": True,
        "—Å—ñ–ª—å": False
    }

    result = {}
    for product in products:
        result[product] = available.get(product, False)
    return result


# --- –§—É–Ω–∫—Ü—ñ—è –¥–ª—è –æ—Ñ–æ—Ä–º–ª–µ–Ω–Ω—è –∑–∞–º–æ–≤–ª–µ–Ω–Ω—è ---
def make_order(order):
    """
    –ü—Ä–∏–π–º–∞—î –∑–∞–º–æ–≤–ª–µ–Ω–Ω—è (—Å–ª–æ–≤–Ω–∏–∫ —Ç–æ–≤–∞—Ä—ñ–≤ —ñ–∑ —Ü—ñ–Ω–∞–º–∏).
    –Ø–∫—â–æ –≤—Å—ñ —Ç–æ–≤–∞—Ä–∏ —î ‚Äî –≤–∏–≤–æ–¥–∏—Ç—å —Å—É–º—É.
    –Ü–Ω–∞–∫—à–µ –ø–æ–∫–∞–∑—É—î, —è–∫–∏—Ö —Ç–æ–≤–∞—Ä—ñ–≤ –Ω–µ–º–∞—î.
    """
    availability = check_availability(*order.keys())

    if all(availability.values()):
        total = sum(order.values())
        return f"‚úÖ –ó–∞–º–æ–≤–ª–µ–Ω–Ω—è –ø—ñ–¥—Ç–≤–µ—Ä–¥–∂–µ–Ω–æ! –ó–∞–≥–∞–ª—å–Ω–∞ {format_price(total)}"
    else:
        not_available = [p for p, a in availability.items() if not a]
        return f"‚ùå –¶—ñ —Ç–æ–≤–∞—Ä–∏ –≤—ñ–¥—Å—É—Ç–Ω—ñ: {', '.join(not_available)}"


# --- –ì–æ–ª–æ–≤–Ω–∞ —Ñ—É–Ω–∫—Ü—ñ—è ---
def main():
    """
    –û—Å–Ω–æ–≤–Ω–∞ —á–∞—Å—Ç–∏–Ω–∞ –ø—Ä–æ–≥—Ä–∞–º–∏.
    –ö–æ—Ä–∏—Å—Ç—É–≤–∞—á –º–æ–∂–µ –≤–∏–±—Ä–∞—Ç–∏: –ø–µ—Ä–µ–≥–ª—è–Ω—É—Ç–∏ —Ü—ñ–Ω—É –∞–±–æ –∫—É–ø–∏—Ç–∏.
    """
    print("üõí –í—ñ—Ç–∞—î–º–æ –≤ –º–∞–≥–∞–∑–∏–Ω—ñ!")
    print("–î–æ—Å—Ç—É–ø–Ω—ñ —Ç–æ–≤–∞—Ä–∏: —Ö–ª—ñ–±, –º–æ–ª–æ–∫–æ, —è–±–ª—É–∫–∞, —Ü—É–∫–æ—Ä, —Å—ñ–ª—å")

    # –°–ª–æ–≤–Ω–∏–∫ –∑ —Ü—ñ–Ω–∞–º–∏
    prices = {
        "—Ö–ª—ñ–±": 20,
        "–º–æ–ª–æ–∫–æ": 35,
        "—è–±–ª—É–∫–∞": 25,
        "—Ü—É–∫–æ—Ä": 28,
        "—Å—ñ–ª—å": 15
    }

    # –ü—ñ–¥—Ç—Ä–∏–º–∫–∞ —Ç—Ä–∞–Ω—Å–ª—ñ—Ç–µ—Ä–∞—Ü—ñ—ó
    aliases = {
        "hlib": "—Ö–ª—ñ–±",
        "moloko": "–º–æ–ª–æ–∫–æ",
        "yabluka": "—è–±–ª—É–∫–∞",
        "cukor": "—Ü—É–∫–æ—Ä",
        "sil": "—Å—ñ–ª—å"
    }

    print("\n–û–±–µ—Ä—ñ—Ç—å –¥—ñ—é:")
    print("1 ‚Äî –ü–µ—Ä–µ–≥–ª—è–Ω—É—Ç–∏ —Ü—ñ–Ω–∏")
    print("2 ‚Äî –ö—É–ø–∏—Ç–∏ —Ç–æ–≤–∞—Ä–∏")

    choice = input("–í–∞—à –≤–∏–±—ñ—Ä: ")

    if choice == "1":
        print("\nüìã –¶—ñ–Ω–∏ –Ω–∞ —Ç–æ–≤–∞—Ä–∏:")
        for product, price in prices.items():
            print(f"{product.capitalize()}: {format_price(price)}")

    elif choice == "2":
        user_input = input("\n–í–≤–µ–¥—ñ—Ç—å —Ç–æ–≤–∞—Ä–∏, —è–∫—ñ —Ö–æ—á–µ—Ç–µ –∫—É–ø–∏—Ç–∏ (—á–µ—Ä–µ–∑ –∫–æ–º—É): ").lower()
        # –ü—ñ–¥—Ç—Ä–∏–º–∫–∞ —É–∫—Ä–∞—ó–Ω—Å—å–∫–∏—Ö —ñ –ª–∞—Ç–∏–Ω—Å—å–∫–∏—Ö –Ω–∞–∑–≤
        chosen = [aliases.get(p.strip(), p.strip()) for p in user_input.split(",") if p.strip()]

        # –§–æ—Ä–º—É—î–º–æ –∑–∞–º–æ–≤–ª–µ–Ω–Ω—è –¥–ª—è –≤–∏–±—Ä–∞–Ω–∏—Ö —Ç–æ–≤–∞—Ä—ñ–≤
        order = {product: prices.get(product, 0) for product in chosen if product in prices}

        if not order:
            print("‚ö†Ô∏è –í–∏ –Ω–µ –≤–∏–±—Ä–∞–ª–∏ –∂–æ–¥–Ω–æ–≥–æ —Ç–æ–≤–∞—Ä—É –∑—ñ —Å–ø–∏—Å–∫—É!")
        else:
            print(make_order(order))

    else:
        print("‚ùå –ù–µ–≤—ñ—Ä–Ω–∞ –∫–æ–º–∞–Ω–¥–∞!")


# --- –ó–∞–ø—É—Å–∫ –ø—Ä–æ–≥—Ä–∞–º–∏ ---
if __name__ == "__main__":
    main()
